#for loop
fruits =["Apple", "Peach","Pear"]
for fruit in fruits: # go through each item in the list and print each one
    print(fruit)
    print(fruit + " Pie!!")
    print(fruits) # by putting fruits list in the loop its gonna execute multiple times as much as the items in the list
print((fruits)) # but this one only prints one time cause its outside the loop

for char in fruits: # this means for each char in fruits do the below
    print(char)


